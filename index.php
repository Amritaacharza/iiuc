													<h1 align="center" style="color:#005C25;font-weight:bold;">IIUC Central Library</h1>
											</div>
					<div>
						<p style="font-size:1em;" align="justify">
						Since its inception in 1995, International Islamic University Chittagong (IIUC) authority has been taking all possible steps to build up a well-equipped modern library. Now it becomes one of the most extensive libraries among all the private university libraries of Bangladesh. Besides, the university has a Female Academic Zone Library and two seminar libraries (Pharmacy Seminar Library and MBA Seminar Library). The libraries are running under Library and Information Division (LID). The library can accommodate easily over 456 users at a time in its full air-conditioned reading room. The library operates a network of CCTV camera inside the library to ensure the security of itself and also its resources. The library collections are being arranged according to the DDC scheme.
						<br/><br/>
						The library is now fully automated through KOHA Integrated Library System. The IIUC Digital Institutional Repository is developed using DSpace software where all the publications are being published. To help the research-related activities of the university, the services of Turnitin plagiarism checker, Grammarly grammar checker, EBSCO discovery service, RemoteXs remote access service to e-resources, Zotero reference and citation management software are available at IIUC Central Library. 
						<br/><br/>
						The library has about 85500 books of 12000 titles, 1750 journals, and 515 audio-visual materials. Access and download facilities of about 35000 online journals, 15500 eBooks, and four million e-Theses are available. In addition to these collections, the library subscribes printed issues of a large number of journals, foreign and local magazines, and almost all leading national newspapers of Bangladesh.
